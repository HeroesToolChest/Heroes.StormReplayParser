namespace Heroes.StormReplayParser.Replay;

/// <summary>
/// Specifies the game speed.
/// </summary>
public enum StormGameSpeed
{
    /// <summary>
    /// Indicates an unknown game speed.
    /// </summary>
    Unknown = 0,

    /// <summary>
    /// Indicates a slower game speed.
    /// </summary>
    Slower = 1,

    /// <summary>
    /// Indicates a slow game speed.
    /// </summary>
    Slow = 2,

    /// <summary>
    /// Indicates a normal game speed.
    /// </summary>
    Normal = 3,

    /// <summary>
    /// Indicates a fast game speed.
    /// </summary>
    Fast = 4,

    /// <summary>
    /// Indicates a faster game speed.
    /// </summary>
    Faster = 5,
}
